// Write a program to check if a number is positive, negative, or zero.

let num = parseInt(prompt("type a number"))
 if (num > 0 ){
       console.log(num + " is a Positive number")
} else if(num < 0){
        console.log(num +" is a Negative number")
} else if(num === 0){
        console.log("Zero")
}

